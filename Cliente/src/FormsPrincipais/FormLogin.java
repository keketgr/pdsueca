    private void btnCriarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarUserActionPerformed
        //jogo.enviaMensagem("nick", 2, null);

        //jogo.enviaMensagem("nick", 2, null);
        if (TB_username.getText().isEmpty()) {
            lbaviso.setText("Introduza um username");
            lbaviso.setForeground(Color.red);
            return;
        }
        if (TB_password.getText().isEmpty()) {
            lbaviso.setText("Introduza uma password");
            lbaviso.setForeground(Color.red);
            return;
        }

        if (grafico.getComunicacao().getServer() == null) {
            grafico.getComunicacao().createSocket();
            if (grafico.getComunicacao().getServer() == null) {
                lbaviso.setText("Servidor nao encontrado. Tente mais tarde");
                lbaviso.setForeground(Color.red);
                return;
            }
        }
        grafico.getComunicacao().CriarUser(TB_username.getText(), TB_password.getText());
        if (grafico.getComunicacao().isSucesso()) {
            grafico.getComunicacao().setUser(TB_username.getText());
            grafico.getchatRoom().setLbusername();
            grafico.showfChatRoom();
            grafico.getComunicacao().start();
        } else {
            lbaviso.setText("Utilizador j√° Registado");
            lbaviso.setForeground(Color.red);
        }
        if (jCBremember.isSelected()) {
            grafico.getOp().addUser(TB_username.getText(), TB_password.getText());
        }



    }//GEN-LAST:event_btnCriarUserActionPerformed

