    public void update() {
        ListaJogadoresAprovados.removeAll();
        ListaJogadoresConvidados.removeAll();

        ListaJogadoresAprovados.setListData(JogadoresAceites.toArray());
        ListaJogadoresConvidados.setListData(JogadoresConvidados.toArray());

        ListaJogadoresAprovados.repaint();
        ListaJogadoresConvidados.repaint();
        if (JogadoresAceites.isEmpty()) {
            return;
        }
        if (JogadoresAceites.get(0).equals(popup.getChatRoom().getGrafico().getComunicacao().getUser())) {
            BtnComecar.setVisible(true);
            BtnCancelar.setText("Cancelar");
        } else {
            BtnComecar.setVisible(false);
            BtnCancelar.setText("Sair");
        }
    }

