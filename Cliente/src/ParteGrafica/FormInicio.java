    public void UpdateListaJogos(String[][] idJogoUser) {//
        if (idJogoUser == null) {
            return;
        }

        ListJogosDisp.removeAll();
        ListJogadores.removeAll();
        if (idJogoUser.length <= 0) {
            return;
        }

        String[] aux = new String[idJogoUser.length];
        jogadores = new String[idJogoUser.length][];
        for (int i = 0; i < idJogoUser.length; i++) {
            aux[i] = idJogoUser[i][0];
            jogadores[i] = new String[idJogoUser[i].length];
            for (int j = 1; j < idJogoUser[i].length; j++) {
                jogadores[i][j - 1] = idJogoUser[i][j];
            }
        }
        ListJogosDisp.setListData(aux);
        //ListJogosDisp.setSelectedIndex(0);
    }

